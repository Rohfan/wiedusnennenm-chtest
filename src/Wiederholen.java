public class Wiederholen {
    public static void main(String[] args) {

        int anzahlStunden = 20;
        int stundenlohn = 15;

        System.out.print("Anzahl der Stunden letzte Woche: ");
        System.out.println(anzahlStunden);
        System.out.println("Stundenlohn: " + stundenlohn);
        System.out.print("Verdienst letzte Woche: ");
        System.out.println(anzahlStunden * stundenlohn);

        short a = 1;
        short b = 2;
        short c = (short) (a + b);
        System.out.println(c);


    }



}
